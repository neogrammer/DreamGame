class Tile
{
public:
    

    Cfg::Textures texture;
    sf::Vector2i rectPos{ 0,0 };
    int w{ 64 }, h{ 64 };
    sf::Vector2f pos{ 0.f,0.f };

    void render(sf::RenderWindow& wnd_)
    {
        sf::Sprite spr(Cfg::textures.get(texture));

        spr.setPosition(pos);
        spr.setTextureRect({ { (int)rectPos.x, (int)rectPos.y }, { w, h } });


        wnd_.draw(spr);
    }
};

class Tileset
{
public:

    Cfg::Textures texture{};
    std::vector<Tile> tiles{};
    int numRows{}, numCols{};

    void setup(int w_, int h_, Cfg::Textures tex_, int numRows_, int numCols_)
    {
        numRows = numRows_;
        numCols = numCols_;
        texture = tex_;
        tiles.clear();
        tiles.reserve(numRows_ * numCols_);

        for (int y = 0; y < numRows; y++)
        {
            for (int x = 0; x < numCols; x++)
            {
                auto& t = tiles.emplace_back(Tile{});
                t.w = w_;
                t.h = h_;
                t.rectPos = { x * w_, y * h_ };
                t.texture = texture;
                t.pos = {0.f,0.f};
            }
        }

    }

    Tile copyTile(int index_)
    {
        return tiles[index_];
    }
};


